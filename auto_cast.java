package hmproject;

public class auto_cast {
	public static void main(String[]args) {
		int myInt=9;
		double myDouble=myInt;
		//automatic casting:int  to double
		System.out.println(myInt);
		System.out.println(myDouble);
		System.out.println(myDouble);
		System.out.println(myInt);
	}

}
